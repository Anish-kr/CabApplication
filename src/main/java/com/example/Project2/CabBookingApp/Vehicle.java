package com.example.Project2.CabBookingApp;


public class Vehicle {

	private String name;
	private String vehcileNo;
	
	public Vehicle(String name, String vehcileNo) {
		super();
		this.name = name;
		this.vehcileNo = vehcileNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getVehcileNo() {
		return vehcileNo;
	}

	public void setVehcileNo(String vehcileNo) {
		this.vehcileNo = vehcileNo;
	}
	
	
    
    
}
